#!/usr/bin/env node
// Regenerate the README benchmark tables from canonical results. Content
// between the BENCH and QUALITY markers is owned by this script; CI runs
// --check and fails if the committed README differs from the regeneration, so
// no hand-typed number can survive a push.
//
// Usage: node scripts/gen-readme-tables.mjs [--check]

import { readFileSync, readdirSync, writeFileSync, existsSync } from "node:fs";
import { join, dirname, resolve } from "node:path";
import { fileURLToPath } from "node:url";

const root = resolve(dirname(fileURLToPath(import.meta.url)), "..");
const resultsDir = join(root, "results");
const readmePath = join(root, "README.md");
const BENCH_START = "<!-- BENCH:START (generated by scripts/gen-readme-tables.mjs; do not edit by hand) -->";
const BENCH_END = "<!-- BENCH:END -->";
const QUAL_START = "<!-- QUALITY:START (generated by scripts/gen-readme-tables.mjs; do not edit by hand) -->";
const QUAL_END = "<!-- QUALITY:END -->";
const MECH_START = "<!-- MECHANISM:START (generated by scripts/gen-readme-tables.mjs; do not edit by hand) -->";
const MECH_END = "<!-- MECHANISM:END -->";
const metalPath = join(root, "docs/metal-pmu.json");

const CTX_LABEL = { 2048: "2k", 8192: "8k", 16384: "16k", 32768: "32k", 131072: "128k" };
const CONFIG_ORDER = ["f16", "q8_0", "q4_0", "q8_0/q4_0", "q4_0/q8_0"];
// Key/value cache precision per config, so key-dominance reads directly.
const KV_LABEL = {
  f16: "K16 V16",
  q8_0: "K8 V8",
  q4_0: "K4 V4",
  "q8_0/q4_0": "K8 V4",
  "q4_0/q8_0": "K4 V8",
};

function fmt(m) {
  return `${m.median.toFixed(2)} ± ${m.stdev.toFixed(2)}`;
}
function pct(q, f) {
  const v = (q.median / f.median - 1) * 100;
  return `${v >= 0 ? "+" : ""}${v.toFixed(1)}%`;
}
function mean(xs) {
  return xs.length ? xs.reduce((a, b) => a + b, 0) / xs.length : null;
}

const files = existsSync(resultsDir)
  ? readdirSync(resultsDir).filter((f) => f.endsWith(".json") && f !== "index.json").sort()
  : [];
const docs = files.map((f) => ({ f, doc: JSON.parse(readFileSync(join(resultsDir, f), "utf8")) })).filter((d) => !d.doc.fixture_note);

// --- Throughput tables (BENCH block) ---
let bench = "";
for (const { f, doc } of docs) {
  const env = doc.environment;
  bench += `\n### ${doc.model.name} (${doc.model.quant}) on ${env.instance_type}\n\n`;
  bench += `Source: \`results/${f}\` · llama.cpp \`${env.llama_cpp_commit.slice(0, 9)}\` · `;
  bench += `${env.vcpus} vCPU ${env.cpu_model} · flash attention on · seed 42 · median ± stdev, first rep discarded\n\n`;
  bench += `| config | context | prefill tok/s | decode tok/s | peak MiB | Δ prefill | Δ decode | Δ memory | N |\n`;
  bench += `|---|---|---:|---:|---:|---:|---:|---:|---:|\n`;
  const byKey = new Map(doc.cells.map((c) => [`${c.config}|${c.context}`, c]));
  const contexts = [...new Set(doc.cells.map((c) => c.context))].sort((a, b) => a - b);
  for (const ctx of contexts) {
    for (const cfg of CONFIG_ORDER) {
      const c = byKey.get(`${cfg}|${ctx}`);
      if (!c) continue;
      const base = byKey.get(`f16|${ctx}`);
      const m = c.metrics;
      const deltas =
        cfg === "f16" || !base
          ? ["baseline", "baseline", "baseline"]
          : [
              pct(m.prefill_tok_s, base.metrics.prefill_tok_s),
              pct(m.decode_tok_s, base.metrics.decode_tok_s),
              pct(m.peak_memory_mb, base.metrics.peak_memory_mb),
            ];
      bench += `| ${cfg} | ${CTX_LABEL[ctx] ?? ctx} | ${fmt(m.prefill_tok_s)} | ${fmt(m.decode_tok_s)} | `;
      bench += `${Math.round(m.peak_memory_mb.median)} | ${deltas[0]} | ${deltas[1]} | ${deltas[2]} | ${m.prefill_tok_s.raw.length} |\n`;
    }
  }
  if (doc.failed_cells?.length) {
    bench += `\nFailed cells (documented, never deleted): ${doc.failed_cells.map((x) => `${x.config}@${CTX_LABEL[x.context] ?? x.context}`).join(", ")}\n`;
  }
}
if (!bench) bench = "\nNo canonical results committed yet.\n";

// --- Retrieval and divergence quality (QUALITY block) ---
// KLD is one value per config (measured at c512, identical across contexts by
// design). NIAH and RULER-lite variable-tracking are per (config, context);
// reported here as the mean pass rate over the context ladder.
let qual = "";
for (const { f, doc } of docs) {
  const withQ = doc.cells.filter((c) => c.quality && typeof c.quality === "object");
  if (!withQ.length) continue;
  const rows = [];
  for (const cfg of CONFIG_ORDER) {
    const cells = withQ.filter((c) => c.config === cfg);
    if (!cells.length) continue;
    const kld = cells.map((c) => c.quality.kld).find((v) => typeof v === "number");
    const niah = mean(cells.map((c) => c.quality.niah).filter((v) => typeof v === "number"));
    const ruler = mean(cells.map((c) => c.quality.ruler_vt).filter((v) => typeof v === "number"));
    rows.push({ cfg, kld, niah, ruler });
  }
  if (!rows.length) continue;
  qual += `\n### ${doc.model.name} retrieval and divergence quality\n\n`;
  qual += `Source: \`results/${f}\` · KL divergence of the quantized-KV output distribution vs the f16 baseline at c512 · NIAH and RULER-lite variable-tracking as mean pass rate over the 2k to 32k ladder\n\n`;
  qual += `| config | key/value | KLD vs f16 | NIAH mean | RULER-vt mean |\n`;
  qual += `|---|---|---:|---:|---:|\n`;
  for (const r of rows) {
    const kld = r.cfg === "f16" ? "baseline" : r.kld != null ? r.kld.toFixed(4) : "n/a";
    const niah = r.niah != null ? r.niah.toFixed(2) : "n/a";
    const ruler = r.ruler != null ? r.ruler.toFixed(2) : "n/a";
    qual += `| ${r.cfg} | ${KV_LABEL[r.cfg] ?? "?"} | ${kld} | ${niah} | ${ruler} |\n`;
  }
}
if (!qual) qual = "\nNo quality battery committed yet.\n";

// --- Metal PMU mechanism (MECHANISM block) ---
// Tier-1 evidence from the Graviton4 metal session, sourced from docs/metal-pmu.json.
function fmtBig(n) {
  return n.toExponential(2).replace("e+", "e");
}
let mech = "";
if (existsSync(metalPath)) {
  const m = JSON.parse(readFileSync(metalPath, "utf8"));
  mech += `\nEvidence tier ${m.evidence_tier}, ${m.counters}. ${m.session}. ${m.method}.\n\n`;
  mech += `| workload | config | IPC | backend-stall cycles | frontend-stall cycles | L1D refills | L2D refills |\n`;
  mech += `|---|---|---:|---:|---:|---:|---:|\n`;
  for (const w of m.workloads) {
    mech += `| ${w.workload} | ${w.config} | ${w.ipc.toFixed(2)} | ${fmtBig(w.backend_stall_r24)} | ${fmtBig(w.frontend_stall_r23)} | ${fmtBig(w.l1d_refill)} | ${fmtBig(w.l2d_refill)} |\n`;
  }
  mech += `\nMeasurement caveats, reported in full:\n\n`;
  for (const c of m.caveats) mech += `- ${c}\n`;
}
if (!mech) mech = "\nNo metal PMU capture committed yet.\n";

function replaceBlock(text, start, end, content) {
  const si = text.indexOf(start);
  const ei = text.indexOf(end);
  if (si === -1 || ei === -1) {
    console.error(`gen-readme-tables: markers not found in README.md (${start.slice(0, 20)}...)`);
    process.exit(1);
  }
  return text.slice(0, si + start.length) + "\n" + content + "\n" + text.slice(ei);
}

const readme = readFileSync(readmePath, "utf8");
let next = replaceBlock(readme, BENCH_START, BENCH_END, bench);
next = replaceBlock(next, QUAL_START, QUAL_END, qual);
next = replaceBlock(next, MECH_START, MECH_END, mech);

if (process.argv.includes("--check")) {
  if (next !== readme) {
    console.error("gen-readme-tables: README tables differ from regeneration. Run: npm run gen:readme");
    process.exit(1);
  }
  console.log("gen-readme-tables: README matches regeneration");
} else {
  writeFileSync(readmePath, next);
  console.log(`gen-readme-tables: wrote tables for ${docs.length} result file(s)`);
}

#!/usr/bin/env node
// Regenerate the README benchmark tables from canonical results. Content
// between the BENCH markers is owned by this script; CI runs --check and
// fails if the committed README differs from the regeneration, so no
// hand-typed number can survive a push.
//
// Usage: node scripts/gen-readme-tables.mjs [--check]

import { readFileSync, readdirSync, writeFileSync, existsSync } from "node:fs";
import { join, dirname, resolve } from "node:path";
import { fileURLToPath } from "node:url";

const root = resolve(dirname(fileURLToPath(import.meta.url)), "..");
const resultsDir = join(root, "results");
const readmePath = join(root, "README.md");
const START = "<!-- BENCH:START (generated by scripts/gen-readme-tables.mjs; do not edit by hand) -->";
const END = "<!-- BENCH:END -->";

const CTX_LABEL = { 2048: "2k", 8192: "8k", 16384: "16k", 32768: "32k", 131072: "128k" };
const CONFIG_ORDER = ["f16", "q8_0", "q4_0", "q8_0/q4_0", "q4_0/q8_0"];

function fmt(m) {
  return `${m.median.toFixed(2)} ± ${m.stdev.toFixed(2)}`;
}
function pct(q, f) {
  const v = (q.median / f.median - 1) * 100;
  return `${v >= 0 ? "+" : ""}${v.toFixed(1)}%`;
}

const files = existsSync(resultsDir)
  ? readdirSync(resultsDir).filter((f) => f.endsWith(".json") && f !== "index.json").sort()
  : [];

let body = "";
for (const f of files) {
  const doc = JSON.parse(readFileSync(join(resultsDir, f), "utf8"));
  if (doc.fixture_note) continue;
  const env = doc.environment;
  body += `\n### ${doc.model.name} (${doc.model.quant}) on ${env.instance_type}\n\n`;
  body += `Source: \`results/${f}\` · llama.cpp \`${env.llama_cpp_commit.slice(0, 9)}\` · `;
  body += `${env.vcpus} vCPU ${env.cpu_model} · flash attention on · seed 42 · median ± stdev, first rep discarded\n\n`;
  body += `| config | context | prefill tok/s | decode tok/s | peak MiB | Δ prefill | Δ decode | Δ memory | N |\n`;
  body += `|---|---|---:|---:|---:|---:|---:|---:|---:|\n`;
  const byKey = new Map(doc.cells.map((c) => [`${c.config}|${c.context}`, c]));
  const contexts = [...new Set(doc.cells.map((c) => c.context))].sort((a, b) => a - b);
  for (const ctx of contexts) {
    for (const cfg of CONFIG_ORDER) {
      const c = byKey.get(`${cfg}|${ctx}`);
      if (!c) continue;
      const base = byKey.get(`f16|${ctx}`);
      const m = c.metrics;
      const deltas =
        cfg === "f16" || !base
          ? ["baseline", "baseline", "baseline"]
          : [
              pct(m.prefill_tok_s, base.metrics.prefill_tok_s),
              pct(m.decode_tok_s, base.metrics.decode_tok_s),
              pct(m.peak_memory_mb, base.metrics.peak_memory_mb),
            ];
      body += `| ${cfg} | ${CTX_LABEL[ctx] ?? ctx} | ${fmt(m.prefill_tok_s)} | ${fmt(m.decode_tok_s)} | `;
      body += `${Math.round(m.peak_memory_mb.median)} | ${deltas[0]} | ${deltas[1]} | ${deltas[2]} | ${m.prefill_tok_s.raw.length} |\n`;
    }
  }
  if (doc.failed_cells?.length) {
    body += `\nFailed cells (documented, never deleted): ${doc.failed_cells.map((x) => `${x.config}@${CTX_LABEL[x.context] ?? x.context}`).join(", ")}\n`;
  }
}
if (!body) body = "\nNo canonical results committed yet.\n";

const readme = readFileSync(readmePath, "utf8");
const si = readme.indexOf(START);
const ei = readme.indexOf(END);
if (si === -1 || ei === -1) {
  console.error("gen-readme-tables: BENCH markers not found in README.md");
  process.exit(1);
}
const next = readme.slice(0, si + START.length) + "\n" + body + "\n" + readme.slice(ei);

if (process.argv.includes("--check")) {
  if (next !== readme) {
    console.error("gen-readme-tables: README benchmark tables differ from regeneration. Run: npm run gen:readme");
    process.exit(1);
  }
  console.log("gen-readme-tables: README matches regeneration");
} else {
  writeFileSync(readmePath, next);
  console.log(`gen-readme-tables: wrote tables for ${files.length} result file(s)`);
}
